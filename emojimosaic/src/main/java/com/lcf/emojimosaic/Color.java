package com.lcf.emojimosaic;

public class Color {

    private int alph = 0;
    private int red = 0;
    private int green = 0;
    private int blue = 0;

    public int getAlph() {
        return alph;
    }

    public void setAlph(int alph) {
        this.alph = alph;
    }

    public int getRed() {
        return red;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public int getGreen() {
        return green;
    }

    public void setGreen(int green) {
        this.green = green;
    }

    public int getBlue() {
        return blue;
    }

    public void setBlue(int blue) {
        this.blue = blue;
    }
}
